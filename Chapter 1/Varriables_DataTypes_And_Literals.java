public class Varriables_DataTypes_And_Literals {
    public static void main(String[] args) {
        int IntegerNumber = 1054775;
        short ShortNumber = 10;
        long LongNumber = 10968791635464757L;
        float FloatNumber = 10.5f;
        double DoubleNumber = 10.5;
        char Character = 'A';
        boolean BooleanValue = true; // or False - As Boolean Value Can Be Either True Or False
        String StringValue = "Hello World!";
        System.out.println("Integer Number - " + IntegerNumber);
        System.out.println("Short Number - " + ShortNumber);
        System.out.println("Long Number - " + LongNumber);
        System.out.println("Float Number - " + FloatNumber);
        System.out.println("Double Number - " + DoubleNumber);
        System.out.println("Character - " + Character);
        System.out.println("Boolean Value - " + BooleanValue);
        System.out.println("String Value - " + StringValue);

    }
}
