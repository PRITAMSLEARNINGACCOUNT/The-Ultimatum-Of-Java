public class Quick_Quiz_1 {
    public static void main(String[] args) {
        int[] Array = { 1, 2, 3, 4, 5 };
        System.out.println("Printing Array Elements In Reverse Order Of The Array :- ");
        for (int i = Array.length - 1; i >= 0; i--) {
            System.out.println(Array[i]);
        }
    }
}
